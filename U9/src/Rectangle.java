public class Rectangle {
  // Attributes
  public double x;
  public double y;
  public int positionInBox;
  public double size;
  // Methods
  public Rectangle(double x, double y, int positionInBox, double size){
    this.x = x;
    this.y = y;
    this.positionInBox = positionInBox;
    this.size = size;
  }
}
